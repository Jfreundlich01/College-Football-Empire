export class Player {
  constructor(
    firstName,
    lastName,
    age,
    year,
    position,
    stats,
    overall,
    location,
    school
  ) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.age = age;
    this.year = year;
    this.position = position;
    this.stats = stats;
    this.overall = overall;
    this.location = location;
    this.school = school;
  }
}
